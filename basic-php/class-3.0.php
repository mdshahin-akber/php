<?php

// assignment operator

$num_1 = 10;

echo $num_1 . '<br>';

$num_2 = $num_1 + 1; 

echo $num_2 . '<br>';

$x = 5;

// $x = $x + 10;
$x += 10; // $x = $x + 10

echo $x . '<br>';

$x -= 5; // $x = $x - 5

echo $x . '<br>';

$x *= 5; // $x = $x * 5
echo $x . '<br>';

$x /= 5; // $x = $x / 5
echo $x . '<br>';

$x %= 4; // $x = $x % 4
echo $x . '<br>';


